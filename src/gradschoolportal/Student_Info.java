    private void stu_file_browseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stu_file_browseActionPerformed
        // TODO add your handling code here:
        int stu_file_browse = stu_file_chooser.showOpenDialog(this);
        if (stu_file_browse == JFileChooser.APPROVE_OPTION) {
            stu_file_error.setText(" ");
            stu_file = stu_file_chooser.getSelectedFile();
            String stu_name = stu_file.getName();
            String stu_file_ext = getFileExtension(stu_file);
            stu_file_path = stu_file.getAbsolutePath();
            if (stu_file_ext.equals("csv") || stu_file_ext.equals("CSV")) {
                stu_file_name.setText(stu_name);
            } else {
                JOptionPane.showMessageDialog(null, "Please select a valid CSV file.");
            }
        }
    }//GEN-LAST:event_stu_file_browseActionPerformed

