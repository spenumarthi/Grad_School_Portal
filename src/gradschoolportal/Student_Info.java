    private void save_filesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_filesActionPerformed
        // TODO add your handling code here:
        if (stu_file == null) {
            stu_file_error.setText("*This file is required.");
        } else {
            if (stc_file == null) {
                stc_file_error.setText("*This file is required.");
            } else {
                Student_Info_Class sicObj = new Student_Info_Class();
                try {
                    BufferedReader br = new BufferedReader(new FileReader(stu_file_path));
                    StringTokenizer st;
                    while ((stu_file_path = br.readLine()) != null) {
                        st = new StringTokenizer(stu_file_path, ",");
                        ArrayList<String> arr = new ArrayList<>();
                        while (st.hasMoreTokens()) {
                            arr.add(st.nextToken());
                        }
                        String id = arr.get(0);
                        String deg = arr.get(1);
                        String year = arr.get(2);
                        sicObj.add_student_info(id, deg, year);
                    }
                } catch (IOException | HeadlessException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
                try {
                    BufferedReader br = new BufferedReader(new FileReader(stc_file_path));
                    StringTokenizer st;
                    while ((stc_file_path = br.readLine()) != null) {
                        st = new StringTokenizer(stc_file_path, ",");
                        ArrayList<String> arr = new ArrayList<>();
                        while (st.hasMoreTokens()) {
                            arr.add(st.nextToken());
                        }
                        String id = arr.get(0);
                        String code = arr.get(1);
                        String name = arr.get(2);
                        String year = arr.get(3);
                        String grade = arr.get(4);
                        sicObj.add_student_course(id, code, name, year, grade);
                    }
                    JOptionPane.showMessageDialog(null, "Data upload completed successfully.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        }
    }//GEN-LAST:event_save_filesActionPerformed

    private static String getFileExtension(File file) {
        String fileName = file.getName();
        if (fileName.lastIndexOf(".") != -1 && fileName.lastIndexOf(".") != 0) {
            return fileName.substring(fileName.lastIndexOf(".") + 1);
        } else {
            return "";
        }
    }

