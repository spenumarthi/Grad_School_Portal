    private void delete_courseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_courseActionPerformed
        // TODO add your handling code here:
        if (code.equals("") || name.equals("")) {
            JOptionPane.showMessageDialog(null, "Please select a course to delete.");
        } else {
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the course?");
            if (confirm == JOptionPane.YES_OPTION) {
                String ccode = courses_table.getValueAt(courses_table.getSelectedRow(), 0).toString();
                Courses_Info_Class cicObj = new Courses_Info_Class();
                if (cicObj.delete_course(ccode)) {
                    JOptionPane.showMessageDialog(null, "Course deleted.");
                    DefaultTableModel dm = new Courses_Info_Class().get_courses();
                    courses_table.setModel(dm);
                }
            }
        }
    }//GEN-LAST:event_delete_courseActionPerformed

